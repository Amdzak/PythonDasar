""" 
Operator Assigment

operator assigment merupakan sebuah operator penyingkat dari sebuah operasi aritmatika
beberapa operator yang bisa di assigmentkan adalah 
    +=      :   memiliki arti a = a + b
    -=      :   Memiliki arti a = a - b
    /=      :   Memiliki arti a = a / b
    *=      :   Memiliki arti a = a * b
    %=      :   Memiliki arti a = a % b
    //=     :   Memiliki arti a = a // b
    **=     :   Memiliki arti a = a ** b
"""

dataA = 2000
dataB = 3

dataA += dataB
print("hasil nilai dari += ",dataA)

dataA -= dataB
print("hasil nilai dari -= ",dataA)

dataA **= dataB
print("hasil nilai dari **= ",dataA)

dataA //= dataB
print("hasil nilai dari //= ",dataA)

dataA /= dataB
print("hasil nilai dari /= ",dataA)

dataA %= dataB
print("hasil nilai dari %= ",dataA)

dataA *= dataB
print("hasil nilai dari *= ",dataA)


